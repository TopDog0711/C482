    public void setThisPart(Part thisPart) {
        this.thisPart = thisPart;
        if(this.thisPart != null)
        {
            this.titleLabel.setText("Modify Part");
            this.partIDTB.setText(   Integer.toString(this.thisPart.getPartID()));
            this.partNameTB.setText(this.thisPart.getName());
            this.partPriceTB.setText(NumberFormat.getCurrencyInstance().format(this.thisPart.getPrice()));
            this.partInvTB.setText(Integer.toString(this.thisPart.getInstock()));
            this.partMaxSpin.setValue(this.thisPart.getMax());
            this.partMinSpin.setValue(this.thisPart.getMin());
            
            if(this.thisPart instanceof InHouse)
            {
                InHouse ih = (InHouse) thisPart;
                this.partMachineIDTB.setText(Integer.toString(ih.getInstock()));
            }else{
                Outsourced os = (Outsourced) thisPart;
                this.partCompanyNameTB.setText(os.getCompanyName());
            }
        }else{
            this.titleLabel.setText("Add Part");
        }
        
    }
    
