    private void  initProductsTable()
    {
        
        ArrayList<Product> prods =  inventory.getProducts();
     
        List<Object[]> list = new ArrayList<Object[]>();
        for (int i = 0; i <prods.size(); i++) {
            list.add(new Object[] { 
                                  prods.get(i).getProductID(),
                                  prods.get(i).getName(),
                                  prods.get(i).getInstock(),
                                  NumberFormat.getCurrencyInstance().format(prods.get(i).getPrice())
                              });

        }
       
        
        prodTable.setModel(new DefaultTableModel(list.toArray(new Object[][] {}), 
                        new String[] {"Product ID", "Product Name", "Inventory Level", "Price/Cost per Unit"}));
    }
    
    
