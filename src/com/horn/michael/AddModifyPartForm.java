    void updatePartType()
    { 
        if(this.inHouseRadioBtn.isSelected())
        {
            this.partTypeLabel.setVisible(false);
            this.partTypeLabel1.setVisible(true);
        
            this.partCompanyNameTB.setVisible(false);
            this.partMachineIDTB.setVisible(true);
            
        }else{
            this.partTypeLabel.setVisible(true);
            this.partTypeLabel1.setVisible(false);
            
            this.partCompanyNameTB.setVisible(true);
            this.partMachineIDTB.setVisible(false);
        }
    }
    
    void initPartType()
    {
        if(this.thisPart instanceof InHouse)
        {
            this.inHouseRadioBtn.doClick();
           
        }else{
            this.outsourcedRadioBtn.doClick();
            
        }
        
        
    }
    

