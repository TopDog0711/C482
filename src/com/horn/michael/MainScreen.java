    private void modifyPartBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifyPartBtnActionPerformed
        // TODO add your handling code here:
       DefaultTableModel partsModel = (DefaultTableModel)partsTable.getModel();
       Vector vec = (Vector)partsModel.getDataVector().elementAt(partsTable.getSelectedRow());
       int partID = (int)vec.elementAt(0);
       Part targetPart = this.inventory.lookupPart(partID);
       

        
        AddModifyPartForm frame = new AddModifyPartForm();
        frame.setThisPart(targetPart);
        frame.setParent(this);
        frame.setInventory(this.inventory);
        frame.setDefaultCloseOperation(DISPOSE_ON_CLOSE );
        frame.pack();
       
        frame.setVisible(true); 
        
    }//GEN-LAST:event_modifyPartBtnActionPerformed

